package com.ssm.pay.common.exception;

/** Action层异常, 继承自BaseException.
* @author Jiang.Li
* @version 2016年4月12日 下午8:40:18
*/
@SuppressWarnings("serial")
public class SsmActionException extends SsmBaseException  {
	public SsmActionException() {
		super();
	}

	public SsmActionException(String message) {
		super(message);
	}

	public SsmActionException(Throwable cause) {
		super(cause);
	}

	public SsmActionException(String message, Throwable cause) {
		super(message, cause);
	}
}
