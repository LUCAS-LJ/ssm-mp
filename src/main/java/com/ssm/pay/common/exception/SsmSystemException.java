@SuppressWarnings("serial")
public class SsmSystemException extends SsmBaseException{
	public SsmSystemException() {
		super();
	}

	public SsmSystemException(String message) {
		super(message);
	}

	public SsmSystemException(Throwable cause) {
		super(cause);
	}

	public SsmSystemException(String message, Throwable cause) {
		super(message, cause);
	}
}
